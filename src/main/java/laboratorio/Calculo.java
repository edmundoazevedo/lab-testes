package laboratorio;

public class Calculo {
	public static double ExecutaCalculoSoma(double Valor1, double Valor2) {
		double Soma = Valor1 + Valor2;
		return Soma;
	}
	
	public static double ExecutaCalculoMultiplicacao(double Valor1, double Valor2) {
		double Multiplicacao = Valor1 * Valor2;
		return Multiplicacao;
	}
	
	public static double ExecutaCalculoSubtracao(double Valor1, double Valor2) {
		double Subtracao = Valor1 - Valor2;
		return Subtracao;
	}
	
	public static double ExecutaCalculoDivisao(double Valor1, double Valor2) {
		double Divisao = Valor1 / Valor2;
		return Divisao;
	}
}

